type AlertProps = {
  isVisible: boolean;
  errorMessage?: string;
  type?: string;
};

export default function Alert({
  isVisible,
  errorMessage = 'Ocurrio un error inesperado, intenta de nuevo',
  type = 'alert',
}: AlertProps) {
  if (isVisible) {
    return (
      <div
        className='mt-2 h-fit content-center border-l-4 border-red-500 bg-red-100 p-3 text-sm text-red-600 '
        role={type}
      >
        <p className='font-bold'>Algo salio mal</p>
        <p>{errorMessage}</p>
      </div>
    );
  }
}
